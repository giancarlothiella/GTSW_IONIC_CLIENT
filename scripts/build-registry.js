#!/usr/bin/env node
/**
 * scripts/build-registry.js
 *
 * Genera src/app/core/config/component-registry.generated.ts a partire da:
 *   - component-registry.ts (registry base: GTSW + demo HRDEMO/TPXDEMO)
 *   - i progetti operativi opzionali (GTR/WFS/DCW) selezionati dal profilo
 *
 * Uso: node scripts/build-registry.js [--profile=NAME]
 *      default profilo: 'dev'
 */

const fs   = require('fs');
const path = require('path');

const ROOT          = path.resolve(__dirname, '..');
const CONFIG_FILE   = path.join(ROOT, 'projects.config.json');
const BASE_REGISTRY = path.join(ROOT, 'src/app/core/config/component-registry.ts');
const OUT_FILE      = path.join(ROOT, 'src/app/core/config/component-registry.generated.ts');
const PROJECTS_DIR  = path.resolve(ROOT, '../projects');

const ALL_OPTIONAL_PROJECTS = ['GTR', 'WFS', 'DCW'];

function getProfileArg() {
  const arg = process.argv.find(a => a.startsWith('--profile='));
  return arg ? arg.split('=')[1] : 'dev';
}

function resolveProfile(profileName, config) {
  const profiles = config.profiles || {};
  if (!(profileName in profiles)) {
    throw new Error(`Profile '${profileName}' not found in projects.config.json`);
  }
  const list = profiles[profileName];
  if (Array.isArray(list) && list.length === 1 && list[0] === '*') {
    return ALL_OPTIONAL_PROJECTS;
  }
  return list;
}

function readBaseRegistryBody() {
  return fs.readFileSync(BASE_REGISTRY, 'utf8');
}

function buildGeneratedSource(profile, projects) {
  const baseSrc = readBaseRegistryBody();

  // Estrai il blocco delle entries del COMPONENT_REGISTRY base.
  // Saltiamo l'eventuale type annotation `: { ... } =` e cerchiamo la `{` che apre l'oggetto literal.
  const startMarker = 'export const COMPONENT_REGISTRY';
  const startIdx = baseSrc.indexOf(startMarker);
  if (startIdx === -1) throw new Error('Cannot find COMPONENT_REGISTRY export in base registry');
  const eqIdx = baseSrc.indexOf('=', startIdx);
  if (eqIdx === -1) throw new Error('Cannot find = after COMPONENT_REGISTRY declaration');
  const openBrace = baseSrc.indexOf('{', eqIdx);
  // trova la } che chiude l'oggetto al primo livello
  let depth = 0, closeBrace = -1;
  for (let i = openBrace; i < baseSrc.length; i++) {
    const c = baseSrc[i];
    if (c === '{') depth++;
    else if (c === '}') {
      depth--;
      if (depth === 0) { closeBrace = i; break; }
    }
  }
  if (closeBrace === -1) throw new Error('Unterminated COMPONENT_REGISTRY object');
  const baseEntries = baseSrc.slice(openBrace + 1, closeBrace).trim();

  const lines = [];
  lines.push('// AUTOGENERATED — do not edit manually');
  lines.push(`// Generated by scripts/build-registry.js with profile: ${profile}`);
  lines.push('');
  for (const p of projects) {
    lines.push(`import { PROJECT_REGISTRY as ${p}_REGISTRY } from '../../../../../projects/${p}/component-registry.${p}';`);
  }
  if (projects.length > 0) lines.push('');
  lines.push('export const COMPONENT_REGISTRY: { [key: string]: () => Promise<any> } = {');
  lines.push(baseEntries);
  for (const p of projects) {
    lines.push(`  ...${p}_REGISTRY,`);
  }
  lines.push('};');
  lines.push('');
  lines.push('export function getComponentLoader(path: string): (() => Promise<any>) | undefined {');
  lines.push('  return COMPONENT_REGISTRY[path];');
  lines.push('}');
  lines.push('');
  lines.push('export function isComponentRegistered(path: string): boolean {');
  lines.push('  return path in COMPONENT_REGISTRY;');
  lines.push('}');
  lines.push('');
  return lines.join('\n');
}

function main() {
  const profile = getProfileArg();
  if (!fs.existsSync(CONFIG_FILE)) {
    console.error(`projects.config.json not found at ${CONFIG_FILE}`);
    process.exit(1);
  }
  const config = JSON.parse(fs.readFileSync(CONFIG_FILE, 'utf8'));
  const projects = resolveProfile(profile, config);

  // Verifica che i progetti richiesti esistano fisicamente
  for (const p of projects) {
    const projectFile = path.join(PROJECTS_DIR, p, `component-registry.${p}.ts`);
    if (!fs.existsSync(projectFile)) {
      console.error(`Project registry not found: ${projectFile}`);
      process.exit(1);
    }
  }

  const out = buildGeneratedSource(profile, projects);
  fs.writeFileSync(OUT_FILE, out, 'utf8');
  console.log(`[build-registry] profile='${profile}' projects=[${projects.join(', ')}] -> ${path.relative(ROOT, OUT_FILE)}`);
}

main();
